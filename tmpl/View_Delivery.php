ПО УКРАИНЕ:

Доставка в другие населенные пункты Украины осуществляется службой "Новая Почта" согласно прайсу
    компании-перевозчика. (приблизительная стоимость доставки малогабаритных товаров 20-30 грн.)

Как оплатить заказ?
Вы можете оплатить заказ наличными средствами в момент доставки товара курьером или наложенным
платежом на складе "Новой Почты".  К оплате наличными принимаются только гривны.

Наложенным платежом при получении посылки в отделении "Новой Почты" (по Украине).  При данном
способе оплаты к стоимости доставки прибавляется 13 грн+2% от суммы заказа за пересылку денег.


