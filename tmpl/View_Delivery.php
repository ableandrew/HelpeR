Через сайт круглосуточно, семь дней в неделю. Для совершения покупки через сайт Вам необходимо
    зарегистрироваться. Регистрация требует совсем немного времени и минимум информации
    (имя, фамилия, e-mail, контактный телефон, пароль).

По телефону +38 (077) 777-77-77 - ПН-ПТ с 10.00 до 18.00. При данном способе заказа необходимо
    продиктовать по телефону оператору список товаров с количеством, которые Вы хотите приобрести,
    а также назвать Ваше имя, контактный телефон и адрес доставки.

